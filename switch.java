public class switch{
    public static void main(String[] args){
        int age =70;

        switch(age) {
            case 40:
            System.out.println("I wil buy a new house");
            break;

            case 60:
            System.out.println("I wil retire");
            break;

            default:
            System.out.println("I wil buy a old car");
        }
    }
}